"use strict";
/**
 * Writes public/js/firebase-init.js from environment variables so the repo
 * never contains a Google Web API key (Netlify secrets scanning).
 *
 * Netlify: set FIREBASE_WEB_API_KEY (or FIREBASE_API_KEY) in Site env.
 * Local: use .env (loaded by dotenv from repo root) or export vars, then npm start.
 */
const fs = require("fs");
const path = require("path");

var root = path.join(__dirname, "..", "..");
var outFile = path.join(root, "public", "js", "firebase-init.js");

try {
  require("dotenv").config({ path: path.join(root, ".env") });
} catch (e) {
  /* dotenv optional */
}

var apiKey = String(
  process.env.FIREBASE_WEB_API_KEY ||
    process.env.FIREBASE_API_KEY ||
    process.env.FIREBASE_CLIENT_APIKEY ||
    ""
).trim();
if (!apiKey) {
  // Keep build green; runtime will show config/auth failure until env is set.
  apiKey = "MISSING_FIREBASE_WEB_API_KEY";
  console.warn(
    "[inject-firebase-config] FIREBASE_WEB_API_KEY is missing. Wrote placeholder key so build can complete."
  );
}

var raw = {
  apiKey: apiKey,
  authDomain: String(process.env.FIREBASE_AUTH_DOMAIN || "").trim(),
  projectId: String(process.env.FIREBASE_PROJECT_ID || "").trim(),
  storageBucket: String(process.env.FIREBASE_STORAGE_BUCKET || "").trim(),
  messagingSenderId: String(process.env.FIREBASE_MESSAGING_SENDER_ID || "").trim(),
  appId: String(process.env.FIREBASE_APP_ID || "").trim(),
  measurementId: String(process.env.FIREBASE_MEASUREMENT_ID || "").trim(),
  adminCustomTokenOverride: String(process.env.FIREBASE_ADMIN_TOKEN_URL || "").trim(),
};

var enc = {};
Object.keys(raw).forEach(function (k) {
  enc[k] = Buffer.from(String(raw[k] || ""), "utf8").toString("base64");
});

var body =
  "/* Auto-generated by netlify/scripts/inject-firebase-config.cjs - do not edit by hand */\n" +
  "(function () {\n" +
  "  var enc = " +
  JSON.stringify(enc, null, 2) +
  ";\n" +
  "  function d(v){ try { return atob(String(v || \"\")); } catch(e){ return \"\"; } }\n" +
  "  window.AVELON_FB = {\n" +
  "    apiKey: d(enc.apiKey),\n" +
  "    authDomain: d(enc.authDomain),\n" +
  "    projectId: d(enc.projectId),\n" +
  "    storageBucket: d(enc.storageBucket),\n" +
  "    messagingSenderId: d(enc.messagingSenderId),\n" +
  "    appId: d(enc.appId),\n" +
  "    measurementId: d(enc.measurementId),\n" +
  "    adminCustomTokenOverride: d(enc.adminCustomTokenOverride)\n" +
  "  };\n" +
  "})();\n";

fs.writeFileSync(outFile, body, "utf8");
console.log("[inject-firebase-config] Wrote", path.relative(root, outFile));
